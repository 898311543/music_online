package model;

public class UrlResponse {
    private String success;
    private String url;

    public String getSuccess() {
        return success;
    }

    public String getUrl() {
        return url;
    }

    public void setSuccess(String success) {
        this.success = success;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
